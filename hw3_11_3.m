
m = 50;
n = 12;

t = linspace(0,1,m);

tmp = fliplr(vander(t));
A = tmp(:,1:n);
b = cos(4*t);

%a-------------------
x1 = (A'*A) \ (A'*b');
%--------------------

%b-------------------
[Q, R] = mgs(A);
v = Q' * b';
x2 = R\v; 
%--------------------

%c-------------------

[W, R] = house(A);
Q = zeros(m, m);
for i = 1:m
    e = zeros(m,1);
    e(i) = 1;
    Q(:,i) = formQx(W,e);
end

vec = b';
for i = 1:n
    vec(i:m)=vec(i:m)-2*W(i:m,i)*((W(i:m,i))'*vec(i:m));
end
x3 = R\vec;

%--------------------

%d-------------------
[Q, R] = qr(A);
v = Q' * b';
x4 = R\v; 
%--------------------

%e-------------------
x5 = A\b';
%--------------------

%f-------------------
[U,S,V] = svd(A);
x6 = V*(S\(U'*b'))
